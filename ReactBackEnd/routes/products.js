router.put('/gallery-images/:id', uploadOptions.array('images', 10), async (req, res) => {
    if (!mongoose.isValidObjectId(req.params.id)) {
        return res.status(400).send('Invalid Product Id');
    }
    const files = req.files;
    let imagesPaths = [];
    const basePath = `${req.protocol}://${req.get('host')}/public/uploads/`;

    if (files) {
        files.map((file) => {
            imagesPaths.push(`${basePath}${file.filename}`);
        });
    }

    const product = await Product.findByIdAndUpdate(
        req.params.id,
        {
            images: imagesPaths
        },
        { new: true }
    );
        
    if (!product) return res.status(500).send('the gallery cannot be updated!');

    res.send(product);
});

router.put("/createReviews/:id", async (req, res) => {
    try {
        const productId = req.params.id;
        const { comment, rating } = req.body;
    
        // Find the product by ID
        const product = await Product.findById(productId);
    
        if (!product) {
          return res.status(404).json({ error: "Product not found" });
        }
    
        // Check if the user has already reviewed the product
        const existingReviewIndex = product.reviews.findIndex(
          (review) => review.user.toString() === req.body.user._id.toString()
        );
    
        if (existingReviewIndex !== -1) {
          // Update the existing review
          product.reviews[existingReviewIndex].rating = rating;
          product.reviews[existingReviewIndex].comment = comment;
        } else {
          // Create a new review object
          const newReview = {
            user: req.body.user._id,
            name: req.body.user.name,
            rating: rating,
            comment: comment,
          };
    
          // Add the new review to the product's reviews array
          product.reviews.push(newReview);
        }
    
        // Update the total number of reviews
        product.numOfReviews = product.reviews.length;
    
        // Calculate the new average rating
        const totalRating = product.reviews.reduce((sum, review) => sum + review.rating, 0);
        product.ratings = totalRating / product.numOfReviews;
    
        // Save the updated product
        const updatedProduct = await product.save();
    
        if (!updatedProduct) {
          return res.status(500).send("The product review cannot be created or updated!");
        } else {
          res.send(updatedProduct);
        }
      } catch (error) {
        console.error("Error creating or updating review:", error);
        res.status(500).json({ error: "Internal Server Error" });
      }
  });

